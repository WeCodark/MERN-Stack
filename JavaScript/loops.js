// Loops - To do something again and again

// // for loop
for (let i = 1; i <= 20; i += 2) {
    console.log(`Happy New Year ${i}`)
}


// // while loop 
let j = 5
while (j >= 1) {
    console.log(`Happy new year ${j}`)
    j -= 2
}


// // do while
let k = 6
do {
    console.log(`Happy New Year ${k}`)
    k++
}
while (k <= 5)

console.log(k)
